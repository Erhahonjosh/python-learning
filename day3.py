age = int(input("Enter your age: "))
if age >= 18:
    print("You are an adult")
else:
    print("You are a minor")

number = int(input("Enter a number: "))
if number % 2 ==0:
    print("This number is even")
else:
    print("This number is odd")

score = int(input("Enter your score: "))
if score >= 70:
    print("Grade: A")
elif score >= 60:
    print("Grade: B")
elif score >= 50:
    print("Grade: C")
else:
    print("Fail")